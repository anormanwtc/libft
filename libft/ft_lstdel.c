#include "libft.h"

void	ft_lstdel(t_list **lst)
{
	if (*lst)
	{
		if ((*lst)->next)
			ft_lstdel(&((*lst)->next));
		ft_lstdelone(lst);
	}
	*lst = NULL;
}
